# Standard library
import threading
import unittest
from unittest.mock import MagicMock, call, patch

# Internal library
from actions.base import ActionState
from actions.grasp import GraspAction


class _GraspTestMixin:
    def setUp(self):
        self._logger_patcher = patch("actions.base.Logger")
        self._logger_patcher.start()
        self._controller = MagicMock()
        self._controller.execute.return_value = True

    def tearDown(self):
        self._logger_patcher.stop()

    def _make(self, device_name="left_hand", grasp_level=1, torque_limit=180):
        return GraspAction(self._controller, device_name, grasp_level, torque_limit)


class TestGraspActionInit(_GraspTestMixin, unittest.TestCase):
    """Tests for GraspAction construction and validation."""

    def test_initial_state_is_idle(self):
        self.assertEqual(self._make().state(), ActionState.IDLE)

    def test_invalid_grasp_level_zero_raises(self):
        with self.assertRaises(ValueError):
            GraspAction(self._controller, "left_hand", 0, 180)

    def test_invalid_grasp_level_five_raises(self):
        with self.assertRaises(ValueError):
            GraspAction(self._controller, "left_hand", 5, 180)

    def test_valid_grasp_levels_accepted(self):
        for level in (1, 2, 3, 4):
            action = GraspAction(self._controller, "left_hand", level, 128)
            self.assertIsNotNone(action)

    def test_torque_limit_negative_raises(self):
        with self.assertRaises(ValueError):
            GraspAction(self._controller, "left_hand", 1, -1)

    def test_torque_limit_over_255_raises(self):
        with self.assertRaises(ValueError):
            GraspAction(self._controller, "left_hand", 1, 256)

    def test_torque_limit_boundary_zero_accepted(self):
        action = GraspAction(self._controller, "left_hand", 1, 0)
        self.assertIsNotNone(action)

    def test_torque_limit_boundary_255_accepted(self):
        action = GraspAction(self._controller, "left_hand", 1, 255)
        self.assertIsNotNone(action)


